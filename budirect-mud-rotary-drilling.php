                        <p class="mb_15 fs_15"> COOL AND CLEAN WATER is a precious gift of nature. Not only humans, but
                            all living beings depend on it. Except for underground water, no water anywhere is cool and
                            clean. Nor is any other supply of fresh water is plentiful. More than 97% of the earth's
                            fresh water is groundwater. Generally, drilled wells can protect the purity of underground
                            water more easily and more securely. Today's water well driller is both the provider and
                            protector of groundwater resources. Different drilling methods are used to tap different
                            groundwater formations. Due to the earth's different geologic formations, the formation
                            under our feet differs from one place to the next. Hence, drilling problems provide the
                            challenges of water well work. To start the work, the driller must know something about the
                            places and soil formation, where ground water occurs, and how it got there to make it
                            available for proper use.

                        </p>
                        <p class="mb_15 fs_15">Rotary Mud Drilling- The principle of Rotary Mud Drilling is based upon a
                            rotating hollow drill stem and has a fluid of mud or other substance flowing through it. The
                            purpose of circulating the fluid through the drill stem is for the removal of the cutting,
                            cooling of the drill stem rotating in the borehole, and sealing of formation walls to
                            prevent caving and water loss.
                            The Rotary Mud Drilling Rig consists of a mast with a sheave pulley mounted at the top to
                            operate a hoist mechanism. A square or keyed drive called a Kelly hangs in the mast by a
                            block assembly. A water swivel is connected at the top of the Kelly, which is connected to a
                            rubber hose or steel with flexible joints as pressure dictates. This Kelly is driven by a
                            rotary table drive assembly which has inserts or blocks to fit the Kelly. The length of the
                            Kelly is larger than the drill rods to drill down a few feet below to enable a section of
                            drill pipe to be added as the hole drilled deeper. Hollow drill rods and rotary bit are
                            attached by the use of a sub at the bottom of the Kelly. Additional drilling rods are added
                            to the string just below the Kelly, as the drill goes deeper. The drill bit has holes in the
                            bottom through which the fluid can flow. Draw works, which iareattached to the rotary table
                            drive, which operates the hoisting mechanism. A mud pump is mounted on the rig, which forces
                            the drilling fluid through the lines to the swivel down the Kelly and out the bottom of the
                            bit.


                        </p>
                        <p class="mb_15 fs_15">Various types of drill bits are used for Rotary Mud Drilling. Star-type
                            drag bit is used in soft unconsolidated formations such as clay and sand. It has fluid
                            courses which tend to jet the formations and the blades are very effective for sticky clays.
                            The cone-type roller bits have two to four cone-type cutters mounted on the roller bearings,
                            which have teeth in varying lengths. The bits having long teeth cutters are used for soft
                            formation to short intermeshing cutters which tend to chip in very hard material. The harder
                            the formations, the cone teeth are. A thumb rule is to rotate soft formation bits at higher
                            speeds and have much lower bit weight. Suitable RPM for soft formation drags bits are from
