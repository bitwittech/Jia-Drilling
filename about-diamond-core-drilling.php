                        <h2 class="fw_black fs_45 mb_20">Diamond Core Drilling</h2>
                        <p class="mb_15 fs_15">Diamond core drilling differs from other drilling methods and uses a
                            rapidly rotating (150 - 1000+ rpm) thin-walled drill string and an annular bit to cut and
                            extract a solid sample (generally 27 to 85 mm diameter but can be maximum up to 200 mm) from
                            depth. Core drilling is useful for the determination of and study of stratigraphic sequences
                            and types of lithology. For geotechnical purposes, the competency of a rock is determined to
                            check if it can withstand engineering structures such as tunnels, buildings, etc. In mineral
                            exploration, a drilled core helps in exposing the rock of interest and the depth at which is
                            located for further analysis of the grade of the minerals of interest in the rock.
                        </p>
                        <p class="mb_15 fs_15">The basic principle of diamond core drilling is that a drilling rig generates high rotation and a force that drives tools consisting of a bit, core barrel assembly, and a series of drill rods into the earth. As the drill bit advances, a cylindrical core of rock progressively fills a double tube core barrel immediately above the drill bit. Core samples are periodically recovered by lowering a cable with an overshot down the drill string attaching it to the top of the inner tube (inner barrel) of the core barrel and winching it to the surface. The inner barrel is fitted with a core lifter mechanism to prevent the core from dropping out during recovery. Water is the usual circulation fluid used to remove the cuttings and cool the drill bit. The core sections are being removed from the inner tube and placed in core trays; a replacement inner tube is lowered into the hole so that drilling can recommence. This is referred to as a wire line system. 
                        </p>
                        <p class="mb_15 fs_15">Rotation speeds during diamond core drilling can vary from 150 to 400 rpm for surface set bits, to more than 1,000 rpm using impregnated bits. An unusual feature of diamond coring is that the annulus (i.e. distance between the rod and wall of the hole) is typically only 3 to 4 mm. Drilling depth greater than 1500 meters can be achieved with diamond core drilling. In diamond core drilling, penetration is much slower than other drilling methods because of the hardness of the rocks usually encountered, and the time involved in retrieving the core at depth. Under average conditions, the rig can produce 30 to 40 m of core per shift, with samples having a high integrity.

                        </p>
                        <p class="mb_15 fs_15">We at Jia International, manufacture the diamond core drilling rig, a truck-mounted rig, and a support truck to carry items such as the rods, casing, fuel, and water, A small version of the diamond core rig we manufacture on a trolley and tractor-mounted also.
                        </p>
